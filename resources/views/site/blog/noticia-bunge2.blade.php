          <article class="post-content">
            <p>
              São Paulo, 25 de abril de 2014. A partir de hoje, o Brasil passa a contar com a nova rota de exportação de grãos, o complexo portuário Miritituba - Barcarena, que envolve a Estação de Transbordo, em Miritituba, e o Terminal Portuário Fronteira Norte (Terfron), localizado em Barcarena, ambos no Pará.

            </p>

            <p>
              Presente no Estado do Pará há mais de dez anos, a Bunge – uma das principais empresas de agronegócio e alimentos do Brasil – investiu R$ 700 milhões na infra-estrutura portuária e na logística, esta última em parceria com a Amaggi. No total deverão ser gerados cerca de 350 empregos diretos e indiretos. “Esse novo investimento permitirá transformar e alavancar o desenvolvimento no norte do país, melhorando a logística de grãos, gerando uma alternativa mais eficiente, econômica e rápida para o escoamento dos grãos brasileiros para o mundo”, explica Pedro Parente, presidente e CEO da Bunge Brasil.

            </p>

            <p>
              Pela nova rota, os grãos das maiores regiões produtoras seguirão por caminhão pela BR-163 até a Estação de Transbordo em Miritituba, no Oeste do Pará, percorrendo uma distância de 1.100 quilômetros. No terminal, a carga será colocada em barcaças que irão navegar o rio Tapajós, passarão pelo estreito de Breves e chegarão ao Terminal Fronteira Norte, em Vila do Conde, Barcarena, um percurso de 1.000 km realizado em aproximadamente três dias. No Terfron, a carga será armazenada para posterior embarque em navios graneleiros, rumo ao exterior.

            </p>

            <p>
              A Bunge também está investindo em um modelo de transporte menos poluente e mais eficiente utilizando a hidrovia Tapajós-Amazonas, por meio da Unitapajós, uma joint venture com o Grupo Amaggi. “Estamos diversificando a matriz logística do país, ainda muito dependente de caminhões e trens. Além disso, com o aumento da demanda mundial por alimentos, a Bunge se coloca numa posição privilegiada ao ganhar uma nova rota de escoamento de grãos, ainda mais sustentável”, afirma Murilo Braz Sant´Anna, vice-presidente de Agronegócio da Bunge Brasil. Um único comboio de 20 barcaças transporta 40 mil toneladas de grãos, o que equivale a mais de 1.000 caminhões ou a 4,5 trens de carga por viagem.

            </p>

            <p>
              “Este é um empreendimento importantíssimo para o Brasil. Com a nova rota, vamos contribuir para o desenvolvimento da região, ao mesmo tempo em que desafogaremos o sistema logístico do Sudeste, que há muito tempo trabalha acima do limite”, comemora Júnior Justino, diretor de logística e Agronegócio da Bunge Brasil.

            </p>

            <strong>Inovação e pioneirismo</strong>

            <p>
              O novo complexo portuário é totalmente automatizado e possui traços que confirmam o pioneirismo da empresa em abrir caminhos para o desenvolvimento. Um exemplo é o inovador sistema de sustentação do píer no terminal de Miritituba, que compensa as variações de níveis do Rio Tapajós. Totalmente isento de obras civis dentro do rio, reduzindo impactos ao meio ambiente, o píer é suspenso por correntes que permitem a movimentação de acordo com o nível da água. Além disso, o processo de carregamento e descarregamento dos grãos é realizado em área coberta, permitindo operação mesmo em dias chuvosos.

            </p>

            <p>
              O Terfron (Terminal Portuário Fronteira Norte), localizado em Barcarena, também é marcado pela inovação. A carga das barcaças que chegam pelo rio será desembarcada de forma contínua, por meio de um equipamento de alta tecnologia, com um moderno sistema de esteiras de carregamento e sugadores especiais.

            </p>

            <p>
              Saindo das barcaças, os grãos são transferidos para silos e armazéns. Já o embarque em navios para exportação acontecerá por três torres de carregamento que transportam a carga desde os armazéns por meio de esteiras 100% controladas por computador, com dispositivos especiais de segurança.
            </p>

            <strong>Destino</strong>

            <p>
              A maior parte da soja e do milho embarcados em Barcarena seguirá para a Ásia e Europa. Até o final de 2015, o Terfron será o segundo maior terminal exportador da Bunge Brasil, com capacidade de escoamento de quatro milhões de toneladas/ano, ficando atrás apenas do TGG (Terminal de Granéis do Guarujá), no Porto de Santos (SP), que exportou cerca de oito milhões de toneladas em 2013.
            </p>

            <strong>Sobre a Bunge:</strong>

            <p>
              Presente no Brasil desde 1905, a Bunge é uma das principais empresas de agronegócio e alimentos do Brasil e uma das maiores exportadoras. Atua de forma integrada, do campo à mesa do consumidor. Desde a compra e processamento de grãos (soja, trigo e milho), produção de alimentos (óleos, margarinas, maioneses, azeite,  farinhas de trigo, molhos e atomatados), serviços portuários até a produção de açúcar e bioenergia. Eleita empresa mais sustentável do agronegócio pelo Guia Exame de Sustentabilidade, a Bunge conta com cerca de 20 mil colaboradores, atuando em mais de 100 instalações, entre fábricas, usinas, moinhos, portos, centros de distribuição e silos, em 19 estados e no Distrito Federal. Marcas como Salada, Soya, Delícia, All Day, Primor, Cardeal, Salsaretti e Bunge Pro estão profundamente ligadas não apenas à história econômica brasileira, mas também aos costumes, à pesquisa científica, ao pioneirismo tecnológico e à formação de gerações de profissionais.
            </p>
