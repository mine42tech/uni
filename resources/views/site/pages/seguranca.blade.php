                    <h1>Prêmio Proteção Brasil</h1>
                    <p>Em 2019, a Unitapajós recebeu o prêmio Proteção Brasil, com o melhor case da região Norte e na categoria Trabalho em Altura. Nossa equipe reinventou a maneira de fazer a manutenção e troca da rosca do Silwertell, que consiste num grande tubo utilizado no descarregamento de grãos transportados em barcaças. O equipamento funciona 24 horas por dia, captando grãos através de um transportador vertical composto por três roscas verticais. No entanto, após algum tempo de uso, essas roscas verticais se desgastam, o que exige a troca das peças. A operação envolvia vários riscos, como o trabalho em altura, riscos ergonômicos, içamento e equipamentos sobre balsa. A substituição das roscas demandava cerca de 40 horas de trabalho, divididas em quatro ou cinco dias, e uma equipe de 10 colaboradores. Além da exposição aos riscos, o processo exigia a parada do equipamento e, por consequência, do descarregamento de grãos no terminal de Barcarena, provocando atrasos nas viagens e queda no volume de armazenamento.</p>
                    <p>Todos esses fatores levaram a equipe da Unitapajós a buscar formas mais seguras e eficientes de fazer manutenção. A solução pioneira veio dos próprios colaboradores da área de manutenção, que desenvolveram uma plataforma de sustentação para o trabalho; uma gaiola para suporte à rosca do equipamento e o uso do guincho do Siwertell como apoio para içamento das roscas. O processo inovador, implementado desde o início de 2019, deixou a operação mais segura e eficiente ao eliminar o trabalho em altura e o uso de equipamentos móveis pesados, além de reduzir a necessidade e quantidade de içamentos de pessoas e materiais. As horas de trabalho também caíram de 40 para 10, em média, exigindo a dedicação de quatro ou cinco empregados, que não precisam acompanhar a atividade em tempo integral.
Este é um exemplo de como traduzimos segurança, nosso maior valor, em iniciativas reais que fazem a diferença na vida das pessoas, em nossas operações e no futuro da empresa. </p>
